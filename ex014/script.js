// Utilizando "for"
//for (inicialização; condição; incremento){}

for(let c = 1; c<=10; c++){
    console.log(c)
}

console.log('FIM!')

/* 
1.`let c = 1;`: Aqui estamos declarando uma variável chamada `c` e a iniciamos com o valor 1. Isso será usado para contar os números de 1 a 10.
2.`c <= 10;`: Essa é a condição do loop. Enquanto `c` for menor ou igual a 10, o loop continuará a ser executado.
3. `c++`: Isso é o que acontece após cada iteração do loop. `c++` significa incrementar `c` por 1. Ou seja, a cada volta do loop, `c` aumenta em 1.
4. `console.log(c);`: Dentro do loop, estamos usando `console.log()` para imprimir o valor atual de `c`. 
*/